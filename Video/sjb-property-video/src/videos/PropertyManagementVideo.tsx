/**
 * SJB Property Group — Property Management Instagram Reel
 * 30 seconds | 1080×1920 | 30fps
 *
 * Scenes:
 *   0–4s   Hook        "Is your property actually working for you?"
 *   4–9s   Pain 1      Void periods + problem tenants
 *   9–14s  Pain 2      Maintenance + compliance
 *   14–21s Solution    SJB handles everything — services list
 *   21–26s Proof       Portfolio images + stats
 *   26–30s CTA         DM "MANAGE"
 *
 * Run `npx tsx src/scripts/generate-pm-assets.ts` first to generate:
 *   public/audio/pm-voiceover.mp3
 *   public/pm-solution-bg.jpg
 */
import {
  AbsoluteFill,
  Audio,
  staticFile,
  useVideoConfig,
} from "remotion";
import { TransitionSeries, linearTiming } from "@remotion/transitions";
import { fade } from "@remotion/transitions/fade";
import { slide } from "@remotion/transitions/slide";
import { loadFont } from "@remotion/google-fonts/Montserrat";
import { PMHookScene } from "./pm/PMHookScene";
import { PMPainScene } from "./pm/PMPainScene";
import { PMSolutionScene } from "./pm/PMSolutionScene";
import { PMProofScene } from "./pm/PMProofScene";
import { PMCtaScene } from "./pm/PMCtaScene";
import { properties } from "../data";

const { fontFamily } = loadFont("normal", {
  weights: ["400", "600", "700", "800", "900"],
  subsets: ["latin"],
});

export const PropertyManagementVideo: React.FC = () => {
  const { fps } = useVideoConfig();

  const TRANSITION = Math.round(0.4 * fps); // 12 frames

  const hookDuration      = Math.round(4 * fps);   // 4s
  const pain1Duration     = Math.round(5 * fps);   // 5s
  const pain2Duration     = Math.round(5 * fps);   // 5s
  const solutionDuration  = Math.round(7 * fps);   // 7s
  const proofDuration     = Math.round(5 * fps);   // 5s
  const ctaDuration       = Math.round(4 * fps);   // 4s

  return (
    <AbsoluteFill style={{ backgroundColor: "#0d1a22", fontFamily }}>

      {/* Voiceover — generated by WaveSpeed ElevenLabs eleven-v3 */}
      <Audio
        src={staticFile("audio/pm-voiceover.mp3")}
        startFrom={0}
        volume={1}
      />

      <TransitionSeries>

        {/* Scene 1 — Hook */}
        <TransitionSeries.Sequence durationInFrames={hookDuration}>
          <PMHookScene />
        </TransitionSeries.Sequence>

        <TransitionSeries.Transition
          presentation={fade()}
          timing={linearTiming({ durationInFrames: TRANSITION })}
        />

        {/* Scene 2 — Pain Point 1: Voids + tenants */}
        <TransitionSeries.Sequence durationInFrames={pain1Duration}>
          <PMPainScene
            imageUrl={properties[0].image} // SJB Vienna
            sceneLabel="The Problem"
            painPoints={[
              {
                icon: "📉",
                title: "Void periods",
                subtitle: "Weeks without rent — money straight out of pocket",
              },
              {
                icon: "😤",
                title: "Problem tenants",
                subtitle: "Late payments, damage, legal headaches",
              },
            ]}
          />
        </TransitionSeries.Sequence>

        <TransitionSeries.Transition
          presentation={slide({ direction: "from-right" })}
          timing={linearTiming({ durationInFrames: TRANSITION })}
        />

        {/* Scene 3 — Pain Point 2: Maintenance + compliance */}
        <TransitionSeries.Sequence durationInFrames={pain2Duration}>
          <PMPainScene
            imageUrl={properties[1].image} // SJB Cheadle
            sceneLabel="Sound familiar?"
            painPoints={[
              {
                icon: "📞",
                title: "3am maintenance calls",
                subtitle: "Boilers, leaks, emergencies — always your problem",
              },
              {
                icon: "⚖️",
                title: "Compliance risk",
                subtitle: "HMO licences, deposit rules, safety certificates",
              },
            ]}
          />
        </TransitionSeries.Sequence>

        <TransitionSeries.Transition
          presentation={fade()}
          timing={linearTiming({ durationInFrames: TRANSITION })}
        />

        {/* Scene 4 — Solution */}
        <TransitionSeries.Sequence durationInFrames={solutionDuration}>
          <PMSolutionScene />
        </TransitionSeries.Sequence>

        <TransitionSeries.Transition
          presentation={slide({ direction: "from-left" })}
          timing={linearTiming({ durationInFrames: TRANSITION })}
        />

        {/* Scene 5 — Proof */}
        <TransitionSeries.Sequence durationInFrames={proofDuration}>
          <PMProofScene />
        </TransitionSeries.Sequence>

        <TransitionSeries.Transition
          presentation={fade()}
          timing={linearTiming({ durationInFrames: TRANSITION })}
        />

        {/* Scene 6 — CTA */}
        <TransitionSeries.Sequence durationInFrames={ctaDuration}>
          <PMCtaScene />
        </TransitionSeries.Sequence>

      </TransitionSeries>
    </AbsoluteFill>
  );
};
